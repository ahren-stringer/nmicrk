<p>
 <span style="color:#2E74B5;"><em>Кремнистые ванны</em></span>. Кремнистые воды встречаются в традиционных курортных местах - Кавказские минеральные воды, Белокуриха, Кульдур, Дарасун и др. Характерной особенностью этих вод является наличие в них газов - углекислый газ, сероводород, радон. Курс кремнистых ванн способствует улучшению системного кровотока, снижению содержания в крови липопротеидов низкой плотности, нормализуют показатели клеточного и гуморального иммунитета. Кремнистые ванны с успехом используются в программах активного долголетия.
</p>
<p>
 <span style="color:#2E74B5;"><em>Сероводородные ванны</em></span>. Сероводородные воды широко распространены в природе. Лечебное действие сероводородных вод определяется присутствующим в воде свободным сероводородом и гидросульфидными ионами. Корректно назначенный курс сероводородных ванн вызывает уменьшение сопротивления периферических сосудов, снижение артериального давления, усиление кровоснабжения тканей, основного обмена, ускорению процессов восстановления. Они противопоказаны при нарушениях сердечного ритма. Сероводородные ванны оказывают выраженное сосудорасширяющее, противовоспалительное, обезболивающее, иммуностимулирующее действие, способствуют улучшению нервной проводимости и состояния центральной нервной системы.
</p>
