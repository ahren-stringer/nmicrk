<p style="text-align:center;">
 <strong>Бальнеотерапия</strong>
</p>
<p>
 <span style="color:#2E74B5;"><em>Хлоридные натриевые ванны</em></span>. Хлоридные натриевые - наиболее часто встречающиеся в природе минеральные воды. Хлоридные натриевые ванны действуют за счет оседания минеральной соли на кожу и образования на ней так называемого солевого плаща. Проникновение электролитов из воды ванны в поверхностные слои кожи на длительное время меняет среду кожи: нормализует её реактивность, обмен веществ, процессы микроциркуляции. Хлоридные натриевые ванны способствуют расширению периферических сосудов, усилению кровообращения, увеличению поглощения организмом кислорода, функции желез внутренней секреции. Хлоридные натриевые ванны в большей степени, чем все другие, улучшают тонус вен и стимулируют венозный кровоток.
</p>
<p>
 <span style="color:#2E74B5;"><em>Йодобромные ванны</em></span>. Одна из популярных процедур бальнеотерапии. Доказано проникновение из воды ванны через кожу йода или брома. Йодобромные ванны оказывают на организм седативное и обезболивающее действие, способствуют снижению свертываемости крови, положительно влияют на функцию эндокринных желез (щитовидной, половых желез и надпочечников), на нарушенный липидный обмен.
</p>
<p>
 <span style="color:#2E74B5;"><em>Углекислые ванны</em></span>. Относятся к газовым ваннам, т.е. к ваннам из воды, перенасыщенной газом, в данном случае углекислым.
</p>
