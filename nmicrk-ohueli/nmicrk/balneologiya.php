<p>
	 ПДМ активно влияет на организм человека: стимулирует адаптационные возможности нервной, сердечно-сосудистой, дыхательной, эндокринной, иммунной систем, улучшает функциональное состояние кожи, опорно-двигательного аппарата.
</p>
<p>
 <span style="color:#2E74B5;"><em>Душ Шарко (струевой душ)</em></span> - одна из самых востребованных процедур гидропатии. Наиболее эффективные результаты воздействия струевого душа отмечены при следующих состояниях: избыточная масса тела и ожирение, гипотоническая болезнь, болезни костно-мышечной системы, астенические состояния, вегето-сосудистая дистония, синдром хронической усталости и др.
</p>
<p>
 <span style="color:#2E74B5;"><em>Каскадный душ</em></span> - обливной душ, вода от которого ниспадает потоком различной интенсивности, температуры и формы. Каскадный душ принимается в неглубоком бассейне, над которым установлено оборудование, подающее воду. Пациент встает в воде примерно по пояс, и сверху на него льется поток воды индифферентной температуры, которая в первую очередь воздействует на зоны головы (в основном, на теменную и затылочную), шейного отдел позвоночника, активизируя кровообращение в воротниковой зоне, усиливая приток крови к головному мозгу. Изменяя интенсивность давления и температуру душа можно получить тонизирующий, спазмолитический, седативный эффект.
</p>
<p>
 <span style="color:#2E74B5;"><em>Вихревые ванны</em></span>. Это ванны с турбулентным движением воды, которое создается специальным устройством. Мягкий массаж тканей улучшает трофику и проводимость по нервно-мышечным волокнам. Происходит нормализация периферического кровообращения, микроциркуляторных процессов и состояния вегетативной нервной системы. Вихревые ванны вызывают тонизирующее, спазмолитическое, обезболивающее действие.
</p>
<p>
 <span style="color:#2E74B5;"><em>Гидрогальванические ванны</em></span> - уникальная процедура в которой сочетается действие пресной ванны и электрического тока невысокого напряжения и небольшой силы. Возможность использования различных режимов (гальванический, диадинамический и др.) позволяет добиться обезболивающего, миостимулирующего, сосудорасширяющего и др. эффектов. При корректном назначении эти ванны могут быть очень эффективными у больных сдорсопатиями, сахарным диабетом с ангио- и нейропатиями.
</p>
