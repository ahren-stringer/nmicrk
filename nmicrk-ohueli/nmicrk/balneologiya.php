<h1 class="baln_title_3">
Сероводородные ванны
</h1>
<p>
Сероводородные воды широко распространены в природе. Лечебное действие сероводородных вод определяется присутствующим в воде свободным сероводородом и гидросульфидными ионами. Корректно назначенный курс сероводородных ванн вызывает уменьшение сопротивления периферических сосудов, снижение артериального давления, усиление кровоснабжения тканей, основного обмена, ускорению процессов восстановления. Они противопоказаны при нарушениях сердечного ритма. Сероводородные ванны оказывают выраженное сосудорасширяющее, противовоспалительное, обезболивающее, иммуностимулирующее действие, способствуют улучшению нервной проводимости и состояния центральной нервной системы.
</p>
